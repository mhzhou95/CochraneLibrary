package com.github.mhzhou95;

import java.net.URL;

public class Topic {
    private URL url;
    private String topic;

    public URL getUrl() {
        return url;
    }

    public void setUrl(URL url) {
        this.url = url;
    }

    public String getTopic() {
        return topic;
    }

    public void setTopic(String topic) {
        this.topic = topic;
    }
}
